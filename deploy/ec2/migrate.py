INSERT_RE = re.compile(
    r"^INSERT\s+INTO\s+`(?P<table>\w+)`\s*\((?P<cols>[^)]+)\)\s*VALUES\s*\((?P<vals>.*)\)\s*;?\s*$",
    re.DOTALL | re.IGNORECASE,
)


def parse_values_tuple(s: str) -> list[str]:
    """
    Split a VALUES row like: "'abc','def',NULL,42,0.5"
    into raw token strings (strings keep their surrounding quotes).
    Respects MySQL-style escapes inside strings: \\', \\\\, ''.
    """
    tokens: list[str] = []
    i, n = 0, len(s)
    while i < n:
        while i < n and s[i] in " \t\n\r":
            i += 1
        if i >= n:
            break
        if s[i] == "'":
            # Scan closing quote, handling escapes
            j = i + 1
            while j < n:
                c = s[j]
                if c == "\\" and j + 1 < n:
                    j += 2
                    continue
                if c == "'":
                    if j + 1 < n and s[j + 1] == "'":
                        j += 2
                        continue
                    break
                j += 1
            if j >= n:
                raise ValueError(f"unterminated string at {i}: {s[i:i+40]!r}")
            tokens.append(s[i : j + 1])
            i = j + 1
        else:
            j = i
            depth = 0
            while j < n:
                c = s[j]
                if c == "," and depth == 0:
                    break
                if c == "(":
                    depth += 1
                elif c == ")":
                    depth -= 1
                j += 1
            tokens.append(s[i:j].strip())
            i = j
        # Skip the comma
        if i < n and s[i] == ",":
            i += 1
    return tokens


def mysql_unescape_string(lit: str) -> str:
    """Convert a MySQL string literal (with surrounding ') to a Python str."""
    assert lit.startswith("'") and lit.endswith("'"), f"not a string literal: {lit[:40]!r}"
    inner = lit[1:-1]
    out: list[str] = []
    i, n = 0, len(inner)
    while i < n:
        c = inner[i]
        if c == "\\" and i + 1 < n:
            nxt = inner[i + 1]
            escapes = {
                "n": "\n", "t": "\t", "r": "\r", "0": "\x00",
                "Z": "\x1a", "b": "\b", "\\": "\\", "'": "'", '"': '"',
            }
            out.append(escapes.get(nxt, nxt))
            i += 2
        elif c == "'" and i + 1 < n and inner[i + 1] == "'":
            out.append("'")
            i += 2
        else:
            out.append(c)
            i += 1
    return "".join(out)


def mysql_literal_to_python(tok: str) -> Any:
    """Convert a single MySQL VALUES token to a Python value."""
    tok = tok.strip()
    if not tok:
        return None
    if tok.upper() == "NULL":
        return None
    if tok.startswith("'"):
        return mysql_unescape_string(tok)
    if tok.startswith(("0x", "0X")) and all(c in "0123456789abcdefABCDEF" for c in tok[2:]):
        return bytes.fromhex(tok[2:])
    # Numeric
    if re.fullmatch(r"-?\d+", tok):
        return int(tok)
    if re.fullmatch(r"-?\d+\.\d*", tok) or re.fullmatch(r"-?\.\d+", tok):
        return float(tok)
    # Scientific or unexpected — pass through as str
    return tok


@dataclass
class ParsedInsert:
    table: str
    columns: list[str]
    values: list[Any]


def parse_insert(stmt: str) -> ParsedInsert | None:
    """Parse a single INSERT statement from the mysqldump."""
    m = INSERT_RE.match(stmt)
    if not m:
        return None
    table = m.group("table")
    cols = [c.strip().strip("`") for c in m.group("cols").split(",")]
    raw_vals = parse_values_tuple(m.group("vals"))
    if len(raw_vals) != len(cols):
        raise ValueError(
            f"column/value count mismatch for {table}: "
            f"{len(cols)} cols vs {len(raw_vals)} values"
        )
    bool_cols = BOOLEAN_COLUMNS.get(table, set())
    python_vals: list[Any] = []
    for col, raw in zip(cols, raw_vals):
        val = mysql_literal_to_python(raw)
        if col in bool_cols:
            if val is None:
                python_vals.append(None)
            elif isinstance(val, int):
                python_vals.append(bool(val))
            elif isinstance(val, str) and val in ("0", "1"):
                python_vals.append(val == "1")
            else:
                python_vals.append(bool(val))
        else:
            python_vals.append(val)
    return ParsedInsert(table=table, columns=cols, values=python_vals)


def iter_inserts(dump_path: Path) -> Iterator[ParsedInsert]:
    """Yield parsed INSERTs in dump order."""
    with dump_path.open("r", encoding="utf-8", errors="replace") as f:
        sql = f.read()
    for raw in sqlparse.split(sql):
        stmt = raw.strip()
        if not stmt.upper().startswith("INSERT INTO"):
            continue
        parsed = parse_insert(stmt)
        if parsed:
            yield parsed


