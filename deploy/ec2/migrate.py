def qident(name: str) -> str:
    """Quote an identifier for Postgres."""
    return '"' + name.replace('"', '""') + '"'


def drop_all(cur) -> None:
    all_tables = [name for name, _ in DDL] + LEGACY_TABLES
    for t in reversed(all_tables):
        cur.execute(f'DROP TABLE IF EXISTS {qident(t)} CASCADE;')
        print(f"  DROP  {t}")


def create_all(cur) -> None:
    for name, ddl in DDL:
        cur.execute(ddl)
        print(f"  CREATE  {name}")
    for idx in INDEXES:
        cur.execute(idx)
    print(f"  +{len(INDEXES)} indexes")


def load_data(cur, dump_path: Path) -> dict[str, int]:
    """
    Insert data in the order rows appear in the dump (which matches FK-safe
    ordering because mysqldump emits them alphabetically but we respect
    the --extended-insert=FALSE stream; for safety we defer FK checks).
    """
    # Disable triggers (FKs) during bulk load, then re-enable. Session-level.
    cur.execute("SET session_replication_role = 'replica';")

    counts: dict[str, int] = {}
    batch: dict[str, tuple[list[str], list[tuple]]] = {}
    BATCH_SIZE = 500

    def flush_batch(table: str) -> None:
        cols, rows = batch[table]
        if not rows:
            return
        placeholders = ", ".join(["%s"] * len(cols))
        col_sql = ", ".join(qident(c) for c in cols)
        sql = f'INSERT INTO {qident(table)} ({col_sql}) VALUES ({placeholders})'
        cur.executemany(sql, rows)
        counts[table] = counts.get(table, 0) + len(rows)
        batch[table] = (cols, [])

    for parsed in iter_inserts(dump_path):
        if parsed.table not in batch:
            batch[parsed.table] = (parsed.columns, [])
        else:
            # Column order could technically vary per-statement; assert same shape.
            assert batch[parsed.table][0] == parsed.columns, (
                f"column order mismatch within {parsed.table}"
            )
        batch[parsed.table][1].append(tuple(parsed.values))
        if len(batch[parsed.table][1]) >= BATCH_SIZE:
            flush_batch(parsed.table)

    for table in list(batch.keys()):
        flush_batch(table)

    cur.execute("SET session_replication_role = 'origin';")
    return counts


def reset_sequences(cur) -> None:
    for table, col in SEQUENCE_TABLES:
        cur.execute(
            f"""
            SELECT setval(
                pg_get_serial_sequence(%s, %s),
                COALESCE((SELECT MAX({qident(col)}) FROM {qident(table)}), 0) + 1,
                false
            );
            """,
            (qident(table), col),
        )
        print(f"  SEQ    {table}.{col}")


def report(cur) -> None:
    print("\n=== Row counts (PostgreSQL) ===")
    total = 0
    for name, _ in DDL:
        cur.execute(f"SELECT COUNT(*) FROM {qident(name)};")
        c = cur.fetchone()[0]
        total += c
        print(f"  {name:<24} {c:>6}")
    print(f"  {'TOTAL':<24} {total:>6}")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--dump", default=os.environ.get("DUMP_PATH", DEFAULT_DUMP))
    parser.add_argument("--db",   default=os.environ.get("DATABASE_URL", DEFAULT_DB_URL))
    parser.add_argument("--dry-run", action="store_true", help="Print DDL only, don't execute")
    args = parser.parse_args()

    dump_path = Path(args.dump).expanduser()
    if not dump_path.exists():
        sys.exit(f"Dump not found: {dump_path}")

    if args.dry_run:
        print("=== DDL (dry-run) ===")
        for name, ddl in DDL:
            print(f"-- {name}")
            print(ddl.strip(), "\n")
        for idx in INDEXES:
            print(idx)
        return

    print(f"Dump:  {dump_path}  ({dump_path.stat().st_size / 1024:.1f} KB)")
    print(f"DB:    {args.db}")
    print()

    with psycopg.connect(args.db, autocommit=False) as conn:
        with conn.cursor() as cur:
            print("-- Dropping existing tables --")
            drop_all(cur)
            print("\n-- Creating schema --")
            create_all(cur)
            print("\n-- Loading data --")
            counts = load_data(cur, dump_path)
            print("\n-- Resetting sequences --")
            reset_sequences(cur)
        conn.commit()
        print("\nCOMMIT OK.")

    with psycopg.connect(args.db) as conn:
        with conn.cursor() as cur:
            report(cur)

    # Sanity check: the loaded counts should match what COUNT(*) sees.
    print("\n=== Inserts parsed from dump ===")
    for t, c in sorted(counts.items()):
        print(f"  {t:<24} {c:>6}")


if __name__ == "__main__":
    main()
